# store numbers in variables called a and b
a = 3
b = 8

# test if a is greater than b
# if it is, display 'a is greater than b'
# if not, test if a is less than b
# if it is, display 'a is smaller than b
# if not, display 'a is equal to b'
if a > b:
	print('a is greater than b')
elif a < b:
	print('a is smaller than b')
else:
	print('a is equal to b')